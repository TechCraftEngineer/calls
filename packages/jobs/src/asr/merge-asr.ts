/**
 * LLM-объединение результатов нескольких ASR провайдеров:
 * дополняет один транскрипт другим, исправляет ошибки распознавания,
 * объединяет уникальную информацию из всех источников.
 */

import { generateWithAi, hasAiProviderConfigured } from "@calls/ai";
import { createLogger } from "../logger";

const logger = createLogger("asr-merge");

const SYSTEM_PROMPT = `Ты эксперт по объединению транскриптов телефонных разговоров от разных ASR-систем.

ГЛАВНОЕ ПРАВИЛО: ИСПОЛЬЗУЙ ТОЛЬКО СЛОВА ИЗ ИСХОДНЫХ ТРАНСКРИПТОВ!
• ЗАПРЕЩЕНО добавлять слова, которых нет ни в одном источнике
• ЗАПРЕЩЕНО пересказывать своими словами
• ЗАПРЕЩЕНО исправлять грамматику или стиль речи
• ЗАПРЕЩЕНО добавлять вежливые обращения, если их не было
• РАЗРЕШЕНО только выбирать лучший вариант распознавания из исходных источников

ЦЕЛЬ: Создать максимально точный транскрипт, объединив лучшие фрагменты из всех источников.

КРИТИЧЕСКИ ВАЖНО — НЕ ПРОСТО ВЫБИРАЙ ОДИН ВАРИАНТ:
• Анализируй каждую фразу и слово отдельно
• Если в Транскрипте 1 одно слово распознано правильно, а в Транскрипте 2 — другое, объедини их
• Пример: "илья кибер" (Т1) + "Илья компания кюбира" (Т2) → "Илья компания Кибира"
• Не копируй целиком один из транскриптов — это неправильно!

СТРУКТУРА ДИАЛОГА:
• ОБЯЗАТЕЛЬНО разбей текст на реплики по спикерам
• Формат: "Спикер N: текст" (каждая реплика с новой строки)
• Определи смену спикера по контексту (вопрос-ответ, смена темы, паузы)
• Если один транскрипт имеет структуру диалога — используй её как основу для разбиения

АЛГОРИТМ ПОСЛОВНОГО ОБЪЕДИНЕНИЯ:

1. СОПОСТАВЛЕНИЕ РЕПЛИК
   • Найди соответствующие реплики в обоих транскриптах по смыслу
   • Выровняй их по хронологии разговора
   • Определи границы реплик каждого спикера

2. ОБЪЕДИНЕНИЕ ВНУТРИ КАЖДОЙ РЕПЛИКИ (пословно):
   
   Имена и названия компаний:
   • "илья" (Т1) vs "Илья" (Т2) → "Илья" (с заглавной)
   • "кибер" (Т1) vs "кюбира" (Т2) → "Кибира" (правильное название)
   
   Иностранные бренды и термины (КРИТИЧЕСКИ ВАЖНО):
   • "данфосс" (Т1) vs "Danfoss" (Т2) → "Danfoss" (оригинальное написание)
   • "циско" (Т1) vs "Cisco" (Т2) → "Cisco" (латиница)
   • "майкрософт" (Т1) vs "Microsoft" (Т2) → "Microsoft" (латиница)
   • "гугл" (Т1) vs "Google" (Т2) → "Google" (латиница)
   • "эппл" (Т1) vs "Apple" (Т2) → "Apple" (латиница)
   • "амазон" (Т1) vs "Amazon" (Т2) → "Amazon" (латиница)
   • ВСЕГДА выбирай латинское написание для иностранных брендов/терминов
   • Если оба варианта кириллицей — оставь как есть (не выдумывай латиницу)
   
   Пропущенные слова:
   • "Алло. Илья" (Т1) vs "Але илья добрый день меня зовут никита" (Т2)
   • → "Алло. Илья добрый день меня зовут Никита" (объединили всё, БЕЗ добавления запятых)
   
   Искажённые фразы:
   • "неизвестный гидрокопательный кибер" (Т1 — явная ошибка)
   • "компания кюбира" (Т2 — правильно, но с ошибкой в названии)
   • → "компания Кибира" (взяли правильную структуру, исправили название)
   
   Числа и термины:
   • "30 рабочих дней" (Т1) vs "тридцать дней" (Т2) → "30 рабочих дней" (точнее)
   • "компрессор" (Т1) vs "компресор" (Т2) → "компрессор" (правильное написание)

3. РАЗРЕШЕНИЕ КОНФЛИКТОВ:
   • Явная ошибка vs осмысленный текст → выбирай осмысленный
   • Оба варианта правдоподобны → выбирай более полный и естественный
   • Разная капитализация → выбирай с правильными заглавными буквами
   • Кириллица vs латиница для брендов → ВСЕГДА выбирай латиницу

4. СОХРАНЕНИЕ АУТЕНТИЧНОСТИ (КРИТИЧЕСКИ ВАЖНО):
   • НЕ исправляй грамматику разговорной речи
   • НЕ добавляй знаки препинания, если их нет в обоих источниках
   • Сохраняй междометия ("ну", "вот", "угу", "э-э", "але", "алло")
   • НЕ добавляй информацию от себя
   • НЕ пересказывай — только объединяй существующее
   • Сохраняй разговорный стиль: неполные предложения, повторы, паузы

ОБЯЗАТЕЛЬНЫЙ ФОРМАТ ВЫВОДА:
Спикер 1: текст первой реплики
Спикер 2: текст ответной реплики
Спикер 1: следующая реплика
...

НЕДОПУСТИМО: 
• Сплошной текст без разделения на спикеров
• Копирование одного из транскриптов целиком
• Игнорирование правильных фрагментов из второго источника
• Добавление слов, которых нет в исходниках
• Литературная обработка текста
• Кириллическое написание иностранных брендов, если есть латинский вариант

Верни только итоговый транскрипт в формате диалога, максимально близкий к оригинальной речи.`;

/**
 * Объединяет транскрипты от разных ASR с помощью LLM.
 * Если доступен только один — возвращает его без изменений.
 */
export async function mergeAsrWithLlm(input: {
  assemblyaiText?: string;
  yandexText?: string;
  huggingFaceText?: string;
  huggingFaceTexts?: string[];
}): Promise<string> {
  const {
    assemblyaiText = "",
    yandexText = "",
    huggingFaceText = "",
    huggingFaceTexts = [],
  } = input;
  const a = assemblyaiText.trim();
  const y = yandexText.trim();
  const hCandidatesPreDedup = [
    ...huggingFaceTexts.map((t) => t.trim()).filter(Boolean),
    huggingFaceText.trim(),
  ];
  const hCandidates = hCandidatesPreDedup.filter(
    (value, index, arr) => Boolean(value) && arr.indexOf(value) === index,
  );
  const removedDuplicates = hCandidatesPreDedup.length - hCandidates.length;
  if (removedDuplicates > 0) {
    logger.info("Удалены дубликаты транскриптов Hugging Face", {
      beforeCount: hCandidatesPreDedup.length,
      afterCount: hCandidates.length,
      removedDuplicates,
    });
  }
  const texts = [a, y, ...hCandidates].filter(Boolean);

  if (texts.length === 0) return "";
  if (texts.length === 1) return texts[0] ?? "";

  if (!hasAiProviderConfigured()) {
    logger.warn("API ключ AI не задан, возвращаем более длинный вариант");
    return texts.reduce((longest, current) =>
      current.length > longest.length ? current : longest,
    );
  }

  const transcriptBlocks = [
    a && `--- Транскрипт 1 (AssemblyAI) ---\n${a}`,
    y && `--- Транскрипт 2 (Yandex) ---\n${y}`,
    ...hCandidates.map(
      (text, index) =>
        `--- Транскрипт ${3 + index} (Hugging Face ${index + 1}) ---\n${text}`,
    ),
  ]
    .filter(Boolean)
    .join("\n\n");

  const start = Date.now();
  try {
    const { text } = await generateWithAi({
      modelProfile: "longContext",
      system: SYSTEM_PROMPT,
      prompt: `Объедини транскрипты одного разговора в один.

${transcriptBlocks}

--- Итоговый объединённый транскрипт ---`,
      temperature: 0.2,
      maxRetries: 2,
      abortSignal: AbortSignal.timeout(90_000),
      functionId: "asr-merge",
    });

    logger.info("LLM объединение ASR завершено", {
      processingTimeMs: Date.now() - start,
      assemblyaiLength: a.length,
      yandexLength: y.length,
      huggingFaceLength: hCandidates.reduce(
        (sum, item) => sum + item.length,
        0,
      ),
      mergedLength: text.length,
    });

    return text.trim();
  } catch (error) {
    logger.error("Ошибка LLM объединения ASR", {
      error: error instanceof Error ? error.message : String(error),
    });
    return texts.reduce((longest, current) =>
      current.length > longest.length ? current : longest,
    );
  }
}
