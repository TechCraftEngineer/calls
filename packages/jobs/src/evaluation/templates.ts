export const EVALUATION_TEMPLATE_SLUGS = [
  "sales",
  "support",
  "general",
] as const;

export type EvaluationTemplateSlug = (typeof EVALUATION_TEMPLATE_SLUGS)[number];

export interface EvaluationTemplate {
  slug: EvaluationTemplateSlug;
  name: string;
  description: string;
  systemPrompt: string;
}

