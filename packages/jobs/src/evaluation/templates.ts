const DEFAULT_TEMPLATE_SLUG: EvaluationTemplateSlug = "general";

/**
 * Возвращает итоговый system prompt для оценки звонка.
 * @param templateSlug — slug шаблона (sales, support, general)
 * @param customInstructions — опциональные короткие инструкции (1–2 предложения)
 */
export function getEvaluationPrompt(
  templateSlug: EvaluationTemplateSlug | string,
  customInstructions?: string | null,
): string {
  const template =
    EVALUATION_TEMPLATES[templateSlug as EvaluationTemplateSlug] ??
    EVALUATION_TEMPLATES[DEFAULT_TEMPLATE_SLUG];
  const base = template.systemPrompt;
  if (!customInstructions?.trim()) return base;
  return `${base}\n\n## Дополнительные инструкции\n${customInstructions.trim()}`;
}

/**
 * Возвращает список шаблонов для UI (без systemPrompt).
 */
export function getEvaluationTemplatesList(): Array<{
  slug: EvaluationTemplateSlug;
  name: string;
  description: string;
}> {
  return EVALUATION_TEMPLATE_SLUGS.map((slug) => {
    const t = EVALUATION_TEMPLATES[slug];
    return { slug: t.slug, name: t.name, description: t.description };
  });
}
